#!/bin/bash

# ==========================================
# Скрипт для очистки папки сэмплов от дублей
# Оставляет только файлы с индексами 1, 2 и 3.
# Остальное перемещает в папку 'unused_samples'.
# ==========================================

# Целевая папка (относительно места запуска скрипта)
TARGET_DIR="Samples"
BACKUP_DIR="unused_samples"

# Проверяем, существует ли папка с сэмплами
if [ ! -d "$TARGET_DIR" ]; then
    echo "Ошибка: Папка '$TARGET_DIR' не найдена."
    echo "Пожалуйста, запустите скрипт из корня проекта или убедитесь, что папка существует."
    exit 1
fi

# Создаем папку для неиспользуемых файлов, если её нет
mkdir -p "$TARGET_DIR/$BACKUP_DIR"

echo "Начинаю анализ файлов в '$TARGET_DIR'..."

# Переходим в папку с сэмплами
cd "$TARGET_DIR" || exit

# === ЛОГИКА СКРИПТА ===
# Мы ищем файлы, заканчивающиеся на .wav.
# Затем с помощью регулярного выражения (grep) отбираем те,
# у которых перед расширением стоит цифра 4, 5 или больше.
#
# Пояснение regex:
# [0-9]   - любая цифра
# [4-9]   - цифра от 4 до 9
# [0-9]+  - число из двух и более цифр (10, 11 и т.д.)
# (_[0-9]+[4-9]|_[4-9]|_[1-9][0-9]+) - эта комбинация ловит суффиксы типа _4, _5, _10 и т.д.,
#                                      но пропускает _1, _2, _3.

# Находим файлы для перемещения (используем цикл для безопасной обработки имен с пробелами)
find . -maxdepth 1 -type f -name "*.wav" | grep -E "(_[0-9]*[4-9]R?|_[4-9]|_[1-9][0-9]+).wav$" | while read -r file; do
    # Убираем ./ в начале имени файла для красоты
    CLEAN_NAME=$(echo "$file" | sed 's|^\./||')

    echo "Перемещение: $CLEAN_NAME в $BACKUP_DIR/"
    mv "$CLEAN_NAME" "$BACKUP_DIR/$CLEAN_NAME"
done

echo "=========================================="
echo "Готово. Лишние дубли перемещены в '$TARGET_DIR/$BACKUP_DIR/'."
echo "Файлы с суффиксами _1, _2, _3 остались в основной папке."
